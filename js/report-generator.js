import { getStockData, getAllUploadDates, getDailySummary } from './firestore-service.js';
import { computeStockHealth } from './stock-analyzer.js';
import { CATEGORIES } from './constants.js';
import { formatDateDisplay, toCSV, downloadFile, itemStockLevel } from './utils.js';

/** Generate daily stock report data */
export async function generateDailyReport(date) {
  const [lehnga, saree, summary] = await Promise.all([
    getStockData(date, CATEGORIES.LEHNGA),
    getStockData(date, CATEGORIES.SAREE),
    getDailySummary(date)
  ]);
  // Recompute levels per worst colour so old Firestore docs report correctly
  const items = [...lehnga, ...saree].map(i => ({ ...i, stockLevel: itemStockLevel(i) }));
  const health = computeStockHealth(items);

  return {
    date,
    displayDate: formatDateDisplay(date),
    items,
    summary: summary || {},
    health,
    totalSKUs: items.length,
    totalPcs: items.reduce((s, i) => s + (i.totalQty || 0), 0)
  };
}

/** Export report as CSV */
export function exportCSV(items, filename = 'stock-report.csv') {
  const columns = [
    { key: 'sku', label: 'SKU' },
    { key: 'name', label: 'Item Name' },
    { key: 'category', label: 'Category' },
    { key: 'totalQty', label: 'Total Qty' },
    { key: 'stockLevel', label: 'Stock Level' }
  ];

  // Flatten colors
  const rows = items.map(i => ({
    ...i,
    colors: (i.colors || []).map(c => `${c.name}(${c.qty})`).join('; ')
  }));

  const fullCols = [...columns, { key: 'colors', label: 'Colors' }];
  const csv = toCSV(rows, fullCols);
  downloadFile(csv, filename, 'text/csv');
}

/** Export as Excel via SheetJS */
export function exportExcel(items, filename = 'stock-report.xlsx') {
  if (!window.XLSX) { alert('SheetJS not loaded'); return; }

  const rows = items.map(i => ({
    'SKU': i.sku,
    'Item Name': i.name,
    'Category': i.category,
    'Total Qty': i.totalQty,
    'Stock Level': i.stockLevel,
    'Colors': (i.colors || []).map(c => `${c.name} × ${c.qty}`).join(', ')
  }));

  const ws = XLSX.utils.json_to_sheet(rows);
  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, ws, 'Stock Report');
  XLSX.writeFile(wb, filename);
}

/** Export as PDF using jsPDF */
export function exportPDF(items, title = 'Stock Report', date = '') {
  if (!window.jspdf) { alert('jsPDF not loaded'); return; }
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF();

  // Header
  doc.setFillColor(16, 185, 129);
  doc.rect(0, 0, 210, 20, 'F');
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('ZIMONZA DAILY STOCK UPDATE', 14, 13);

  doc.setTextColor(30, 41, 59);
  doc.setFontSize(11);
  doc.setFont('helvetica', 'bold');
  doc.text(title, 14, 32);
  if (date) {
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(100, 116, 139);
    doc.text(`Report Date: ${date}`, 14, 39);
  }

  // Table headers
  let y = 50;
  const colWidths = [25, 60, 20, 20, 20, 45];
  const headers = ['SKU', 'Item Name', 'Category', 'Qty', 'Level', 'Colors'];

  doc.setFillColor(241, 245, 249);
  doc.rect(10, y - 5, 190, 8, 'F');
  doc.setFontSize(8);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(30, 41, 59);
  let x = 14;
  headers.forEach((h, i) => { doc.text(h, x, y); x += colWidths[i]; });
  y += 6;

  doc.setFont('helvetica', 'normal');
  doc.setFontSize(7.5);

  for (const item of items.slice(0, 100)) {
    if (y > 270) { doc.addPage(); y = 20; }

    // Alternate row bg
    if (items.indexOf(item) % 2 === 0) {
      doc.setFillColor(248, 250, 252);
      doc.rect(10, y - 4, 190, 6, 'F');
    }

    doc.setTextColor(30, 41, 59);
    x = 14;
    const colorStr = (item.colors || []).slice(0, 3).map(c => `${c.name}(${c.qty})`).join(', ');
    const vals = [item.sku, item.name?.slice(0, 22) || '—', item.category || '—', item.totalQty, item.stockLevel, colorStr];
    vals.forEach((v, i) => { doc.text(String(v || ''), x, y); x += colWidths[i]; });
    y += 6;
  }

  // Footer
  const pageCount = doc.internal.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setFontSize(7);
    doc.setTextColor(148, 163, 184);
    doc.text(`Generated by Zimonza Stock Update • Page ${i} of ${pageCount}`, 14, 290);
  }

  doc.save(`stock-report-${date || 'report'}.pdf`);
}
