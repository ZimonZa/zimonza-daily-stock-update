// ZIMONZA — PDF → Excel Converter
// Standalone module: no Firebase, no auth.
// Reads text-based PDFs with pdf.js. Stock reports ("READY STOCK
// COLOUR WISE") are extracted as one clean row per item — colour
// fragments (which may sit above/below the item in tall cells) are
// attached to the nearest item. Generic coordinate grid otherwise.
// ═══════════════════════════════════════════════════════════════

import * as pdfjsLib from 'https://cdn.jsdelivr.net/npm/pdfjs-dist@4.7.76/build/pdf.min.mjs';

pdfjsLib.GlobalWorkerOptions.workerSrc =
  'https://cdn.jsdelivr.net/npm/pdfjs-dist@4.7.76/build/pdf.worker.min.mjs';

export const STOCK_HEADERS = ['ITEM NO', 'ITEM NAME', 'SUB LOCATION', 'COLOR', 'READY STOCK', 'SALES PCS', 'BAL PCS'];

// First-field text that marks page chrome (company / page / filter / footer)
const JUNK_RE = /^(KUNTAL|PAGE\b|FILTER|GENERATED BY|READY STOCK COLOUR WISE|WWW\.|HTTP)/i;

function median(nums) {
  if (!nums.length) return 0;
  const s = [...nums].sort((a, b) => a - b);
  const mid = Math.floor(s.length / 2);
  return s.length % 2 ? s[mid] : (s[mid - 1] + s[mid]) / 2;
}

function normalizeItems(rawItems) {
  return rawItems
    .filter(it => it.str && it.str.trim() !== '')
    .map(it => ({
      str: it.str,
      x: it.transform[4],
      y: it.transform[5],
      h: Math.abs(it.transform[3]) || 0,
      w: it.width || 0,
    }));
}

function yTolFor(items) {
  return (median(items.map(i => i.h)) || 6) * 0.6 || 4;
}

/** Group items into visual lines (top of page first → highest y first). */
function clusterRows(items, yTol) {
  const byY = [...items].sort((a, b) => b.y - a.y);
  const rows = [];
  let current = null;
  for (const it of byY) {
    if (!current || Math.abs(it.y - current.y) > yTol) {
      current = { y: it.y, items: [it] };
      rows.push(current);
    } else {
      current.items.push(it);
    }
  }
  return rows;
}

function detectColumns(xs, xTol) {
  const sorted = [...xs].sort((a, b) => a - b);
  const anchors = [];
  for (const x of sorted) {
    const last = anchors[anchors.length - 1];
    if (last == null || x - last.sum / last.n > xTol) anchors.push({ sum: x, n: 1 });
    else { last.sum += x; last.n += 1; }
  }
  return anchors.map(a => a.sum / a.n);
}

function colIndexFor(x, anchors) {
  let best = 0, bestDist = Infinity;
  for (let i = 0; i < anchors.length; i++) {
    const d = Math.abs(x - anchors[i]);
    if (d < bestDist) { bestDist = d; best = i; }
  }
  return best;
}

/** Place a line's items into anchors → array of cell strings (one per anchor). */
function assignRow(rowItems, anchors) {
  const cells = new Array(anchors.length).fill('');
  rowItems
    .sort((a, b) => a.x - b.x)
    .forEach(it => {
      const ci = colIndexFor(it.x, anchors);
      cells[ci] = cells[ci] ? `${cells[ci]} ${it.str}`.trim() : it.str.trim();
    });
  return cells;
}

/**
 * Detect the 7 field x-anchors using first-word tokens, so split/wrapped
 * headers (e.g. "READY" + "STOCK" on two lines) still resolve.
 * Returns [itemNoX, nameX, locX, colorX, readyX, salesX, balX] or null.
 */
function detectStockAnchors(pagesItems) {
  for (const items of pagesItems) {
    let colorX = null, readyX = null, salesX = null, balX = null, locX = null;
    const itemXs = [];
    for (const it of items) {
      const s = it.str.trim().toUpperCase();
      if (s === 'COLOR' || s === 'COLOUR') colorX = it.x;
      else if (s === 'READY' || s === 'READY STOCK') readyX = it.x;
      else if (s === 'SALES' || s === 'SALES PCS') salesX = it.x;
      else if (s === 'BAL' || s === 'BAL PCS') balX = it.x;
      else if (s === 'SUB' || s.startsWith('SUB LOC')) locX = it.x;
      else if (s === 'ITEM' || s === 'ITEM NO' || s === 'ITEM NAME') itemXs.push(it.x);
    }
    if (colorX != null && readyX != null && salesX != null && balX != null && locX != null && itemXs.length >= 2) {
      itemXs.sort((a, b) => a - b);
      return [itemXs[0], itemXs[1], locX, colorX, readyX, salesX, balX];
    }
  }
  return null;
}

const isColourText = (t) => /[()*]/.test(t || '');

// Header band lines consist only of these words (handles wrapped headers
// like "READY" / "STOCK" on separate lines).
const HEADER_WORDS = new Set(['ITEM', 'NO', 'NAME', 'SUB', 'LOCATION', 'COLOR', 'COLOUR', 'READY', 'STOCK', 'SALES', 'PCS', 'BAL']);

function isHeaderBandLine(line) {
  const tokens = line.items.flatMap(it => it.str.trim().toUpperCase().split(/\s+/)).filter(Boolean);
  return tokens.length > 0 && tokens.every(t => HEADER_WORDS.has(t));
}

// Place a stock line's items, routing colour fragments (which contain
// '(', ')' or '*') into the COLOR cell so they can't bleed into SUB LOCATION.
function assignStockRow(rowItems, anchors) {
  const readyX = anchors[4];
  const cells = new Array(anchors.length).fill('');
  [...rowItems].sort((a, b) => a.x - b.x).forEach(it => {
    const s = it.str.trim();
    const idx = (/[()*]/.test(s) && it.x < readyX) ? 3 : colIndexFor(it.x, anchors);
    cells[idx] = cells[idx] ? `${cells[idx]} ${s}` : s;
  });
  return cells;
}

function findItem(items, re) {
  const hit = items.find(it => re.test(it.str.trim()));
  return hit ? hit.str.trim().replace(/\s+/g, ' ') : '';
}

function firstLine(items) {
  if (!items.length) return '';
  const top = [...items].sort((a, b) => b.y - a.y)[0];
  return String(top.str).split(/\n|Phone|GSTIN/)[0].trim();
}

/**
 * Convert a PDF File into a structured result.
 * @param {File} file
 * @param {(page:number, total:number)=>void} onProgress
 */
export async function convertPdfToGrid(file, onProgress) {
  const buf = await file.arrayBuffer();
  const pdf = await pdfjsLib.getDocument({ data: new Uint8Array(buf) }).promise;

  const pagesItems = [];
  const allX = [];
  const charWs = [];
  for (let p = 1; p <= pdf.numPages; p++) {
    if (onProgress) onProgress(p, pdf.numPages);
    const page = await pdf.getPage(p);
    const content = await page.getTextContent();
    const items = normalizeItems(content.items);
    pagesItems.push(items);
    for (const it of items) {
      allX.push(it.x);
      if (it.w > 0 && it.str.length > 0) charWs.push(it.w / it.str.length);
    }
  }

  const anchors = detectStockAnchors(pagesItems);

  // ── Stock-report mode: one clean row per item ─────────────────
  if (anchors) {
    const C = { itemNo: 0, name: 1, loc: 2, colour: 3, ready: 4, sales: 5, bal: 6 };

    const pages = [];
    let totalRows = 0, dataCount = 0;

    pagesItems.forEach((items, i) => {
      const lines = clusterRows(items, yTolFor(items)); // top of page first

      // Isolate the data region: skip the company block (above the header),
      // the header band itself, and the "Generated By" footer.
      let started = false;
      const dataLines = [];
      for (const line of lines) {
        if (isHeaderBandLine(line)) { started = true; continue; }
        if (!started) continue;
        const joined = line.items.map(it => it.str).join(' ').trim();
        if (/^Generated By/i.test(joined)) continue;
        dataLines.push(line);
      }
      // Fallback: no header detected on this page → take all non-chrome lines
      if (!started) {
        for (const line of lines) {
          const joined = line.items.map(it => it.str).join(' ').trim();
          if (joined && !JUNK_RE.test(joined)) dataLines.push(line);
        }
      }

      const records = [];
      const colourLines = [];
      for (const line of dataLines) {
        const cells = assignStockRow(line.items, anchors);
        const itemNo = (cells[C.itemNo] || '').trim();
        const colour = (cells[C.colour] || '').trim();

        if (itemNo && itemNo.toUpperCase() !== 'ITEM NO' && !JUNK_RE.test(itemNo)) {
          records.push({
            y: line.y,
            cells: [
              itemNo,
              (cells[C.name] || '').trim(),
              (cells[C.loc] || '').trim(),
              colour,                       // colour on the item's own line (if any)
              (cells[C.ready] || '').trim(),
              (cells[C.sales] || '').trim(),
              (cells[C.bal] || '').trim(),
            ],
          });
        } else if (colour && isColourText(colour)) {
          colourLines.push({ y: line.y, text: colour });
        }
      }

      // Attach each colour fragment to the nearest item by vertical distance
      for (const cl of colourLines) {
        let best = null, bestDist = Infinity;
        for (const rec of records) {
          const d = Math.abs(cl.y - rec.y);
          if (d < bestDist) { bestDist = d; best = rec; }
        }
        if (best) (best.colourParts || (best.colourParts = [])).push(cl);
      }

      for (const rec of records) {
        if (rec.colourParts && rec.colourParts.length) {
          const parts = rec.colourParts.sort((a, b) => b.y - a.y).map(p => p.text); // top → bottom
          const all = [rec.cells[C.colour], ...parts].filter(Boolean).join(' ').replace(/\s+/g, ' ').trim();
          rec.cells[C.colour] = all;
        }
      }

      const meta = {
        title: firstLine(items),
        filter: findItem(items, /^Filter\b/i),
        banner: findItem(items, /READY STOCK COLOUR WISE/i) || 'READY STOCK COLOUR WISE',
        pageInfo: findItem(items, /^Page\s+\d+\s+of\s+\d+/i),
        generatedBy: findItem(items, /^Generated By/i),
      };

      const rows = records.map(r => r.cells);
      dataCount += rows.length;
      totalRows += rows.length;
      pages.push({ pageNum: i + 1, records: rows, meta });
    });

    return { mode: 'stock', headers: STOCK_HEADERS, pages, totalRows, dataCount };
  }

  // ── Generic coordinate-grid mode ──────────────────────────────
  const xTol = (median(charWs) > 1 ? median(charWs) * 2 : 10);
  const gridAnchors = detectColumns(allX, xTol);
  const pages = [];
  let totalRows = 0;
  pagesItems.forEach((items, i) => {
    const rows = clusterRows(items, yTolFor(items))
      .map(r => assignRow(r.items, gridAnchors))
      .filter(r => r.some(c => c !== ''));
    totalRows += rows.length;
    pages.push({ pageNum: i + 1, records: rows, meta: null });
  });
  return { mode: 'generic', headers: null, maxCols: gridAnchors.length, pages, totalRows, dataCount: totalRows };
}
