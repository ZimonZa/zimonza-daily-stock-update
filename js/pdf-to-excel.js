// ═══════════════════════════════════════════════════════════════
// ZIMONZA — PDF → Excel Converter
// Standalone module: no Firebase, no auth.
// Reads text-based PDFs with pdf.js and reproduces the page layout
// on an x/y coordinate grid. Stock reports ("READY STOCK COLOUR
// WISE") get their wrapped multi-colour lines merged back onto the
// item's row and rows classified for styling.
// ═══════════════════════════════════════════════════════════════

import * as pdfjsLib from 'https://cdn.jsdelivr.net/npm/pdfjs-dist@4.7.76/build/pdf.min.mjs';

pdfjsLib.GlobalWorkerOptions.workerSrc =
  'https://cdn.jsdelivr.net/npm/pdfjs-dist@4.7.76/build/pdf.worker.min.mjs';

export const STOCK_HEADERS = ['ITEM NO', 'ITEM NAME', 'SUB LOCATION', 'COLOR', 'READY STOCK', 'SALES PCS', 'BAL PCS'];

// First-field text that marks a page-chrome row (not item data)
const JUNK_RE = /^(KUNTAL|PAGE\b|FILTER|GENERATED BY|READY STOCK COLOUR WISE)/i;

function median(nums) {
  if (!nums.length) return 0;
  const s = [...nums].sort((a, b) => a - b);
  const mid = Math.floor(s.length / 2);
  return s.length % 2 ? s[mid] : (s[mid - 1] + s[mid]) / 2;
}

function normalizeItems(rawItems) {
  return rawItems
    .filter(it => it.str && it.str.trim() !== '')
    .map(it => ({
      str: it.str,
      x: it.transform[4],
      y: it.transform[5],
      h: Math.abs(it.transform[3]) || 0,
      w: it.width || 0,
    }));
}

function yTolFor(items) {
  return (median(items.map(i => i.h)) || 6) * 0.6 || 4;
}

/** Group items into visual lines (top of page first → highest y first). */
function clusterRows(items, yTol) {
  const byY = [...items].sort((a, b) => b.y - a.y);
  const rows = [];
  let current = null;
  for (const it of byY) {
    if (!current || Math.abs(it.y - current.y) > yTol) {
      current = { y: it.y, items: [it] };
      rows.push(current);
    } else {
      current.items.push(it);
    }
  }
  return rows;
}

/** Cluster left-x positions into column anchors. */
function detectColumns(xs, xTol) {
  const sorted = [...xs].sort((a, b) => a - b);
  const anchors = [];
  for (const x of sorted) {
    const last = anchors[anchors.length - 1];
    if (last == null || x - last.sum / last.n > xTol) anchors.push({ sum: x, n: 1 });
    else { last.sum += x; last.n += 1; }
  }
  return anchors.map(a => a.sum / a.n);
}

function colIndexFor(x, anchors) {
  let best = 0, bestDist = Infinity;
  for (let i = 0; i < anchors.length; i++) {
    const d = Math.abs(x - anchors[i]);
    if (d < bestDist) { bestDist = d; best = i; }
  }
  return best;
}

/** Place a line's items into the supplied anchors → array of cell strings. */
function assignRow(rowItems, anchors) {
  const cells = new Array(anchors.length).fill('');
  rowItems
    .sort((a, b) => a.x - b.x)
    .forEach(it => {
      const ci = colIndexFor(it.x, anchors);
      cells[ci] = cells[ci] ? `${cells[ci]} ${it.str}`.trim() : it.str.trim();
    });
  return cells;
}

/** Locate the stock-report header on any page → x-anchor of each of the 7 fields, or null. */
function detectStockAnchors(pagesItems) {
  for (const items of pagesItems) {
    for (const row of clusterRows(items, yTolFor(items))) {
      const anchors = new Array(STOCK_HEADERS.length).fill(null);
      for (const it of row.items) {
        const idx = STOCK_HEADERS.indexOf(it.str.trim().toUpperCase());
        if (idx !== -1 && anchors[idx] == null) anchors[idx] = it.x;
      }
      if (anchors.every(a => a != null)) return anchors;
    }
  }
  return null;
}

function firstLine(items) {
  if (!items.length) return '';
  const top = [...items].sort((a, b) => b.y - a.y)[0];
  return String(top.str).split(/\n|Phone|GSTIN/)[0].trim();
}

/**
 * Convert a PDF File into a structured result.
 * @param {File} file
 * @param {(page:number, total:number)=>void} onProgress
 */
export async function convertPdfToGrid(file, onProgress) {
  const buf = await file.arrayBuffer();
  const pdf = await pdfjsLib.getDocument({ data: new Uint8Array(buf) }).promise;

  // ── Pass 1: per-page items + global x / char-widths
  const pagesItems = [];
  const allX = [];
  const charWs = [];
  for (let p = 1; p <= pdf.numPages; p++) {
    if (onProgress) onProgress(p, pdf.numPages);
    const page = await pdf.getPage(p);
    const content = await page.getTextContent();
    const items = normalizeItems(content.items);
    pagesItems.push(items);
    for (const it of items) {
      allX.push(it.x);
      if (it.w > 0 && it.str.length > 0) charWs.push(it.w / it.str.length);
    }
  }

  const xTol = (median(charWs) > 1 ? median(charWs) * 2 : 10);
  const globalAnchors = detectColumns(allX, xTol);
  const maxCols = globalAnchors.length;
  const stockAnchors = detectStockAnchors(pagesItems);

  // ── Stock-report mode: full-page replica + colour-line merge ──
  if (stockAnchors) {
    const fieldCols = stockAnchors.map(ax => colIndexFor(ax, globalAnchors));
    const [itemNoCol, , , colorCol, readyCol, salesCol, balCol] = fieldCols;
    const spanEnd = Math.max(colorCol + 1, readyCol); // colour wrap stays within [colorCol, readyCol)

    const pages = [];
    let totalRows = 0, dataCount = 0;

    pagesItems.forEach((items, i) => {
      const rows = [];
      let lastData = null;
      for (const line of clusterRows(items, yTolFor(items))) {
        const cells = assignRow(line.items, globalAnchors);
        const itemNo = (cells[itemNoCol] || '').trim();
        const colorVal = (cells[colorCol] || '').trim();
        const qtyEmpty = [readyCol, salesCol, balCol].every(k => !((cells[k] || '').trim()));
        const onlyColourSpan = cells.every((c, ci) => !c || (ci >= colorCol && ci < spanEnd));

        // Wrapped colour continuation → append to the item's COLOR cell
        if (lastData && !itemNo && colorVal && qtyEmpty && onlyColourSpan) {
          const prev = lastData.cells[colorCol];
          lastData.cells[colorCol] = prev ? `${prev}\n${colorVal}` : colorVal;
          continue;
        }

        let kind;
        if (itemNo.toUpperCase() === 'ITEM NO') kind = 'header';
        else if (!itemNo || JUNK_RE.test(itemNo)) kind = 'other';
        else kind = 'data';

        const row = { cells, kind };
        rows.push(row);
        if (kind === 'data') { lastData = row; dataCount++; }
      }
      totalRows += rows.length;
      pages.push({ pageNum: i + 1, rows });
    });

    return { mode: 'stock', fieldCols, maxCols, title: firstLine(pagesItems[0] || []), pages, totalRows, dataCount };
  }

  // ── Generic coordinate-grid mode ──────────────────────────────
  const pages = [];
  let totalRows = 0;
  pagesItems.forEach((items, i) => {
    const rows = clusterRows(items, yTolFor(items))
      .map(r => ({ cells: assignRow(r.items, globalAnchors), kind: 'plain' }))
      .filter(r => r.cells.some(c => c !== ''));
    totalRows += rows.length;
    pages.push({ pageNum: i + 1, rows });
  });
  return { mode: 'generic', fieldCols: null, maxCols, title: '', pages, totalRows, dataCount: totalRows };
}
